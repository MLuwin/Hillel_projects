// Відображення дня тижня по номеру

Console.WriteLine("Введiть номер дня тижня (вiд 1 до 7):");

// Запит на введення номеру дня
int day = Convert.ToInt32(Console.ReadLine());

// Вибір дня залежно від введеного номеру
string dayName = day switch
{
    1 => "Понедiлок",
    2 => "Вiвторок",
    3 => "Середа",
    4 => "Четвер",
    5 => "П'ятниця",
    6 => "Субота",
    7 => "Недiля",
    _ => "Номер введено не вiрно!"
};

// Відображення вибраного дня
Console.WriteLine(dayName);